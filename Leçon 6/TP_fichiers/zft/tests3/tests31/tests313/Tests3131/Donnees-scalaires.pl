# affichage d'une chaine
print "\nBonjour tout le monde !"; # \n = saut de ligne

# les nombres et les 5 opérations
print "\n2 + 3 = " , (2+3);
print "\n2 - 3 = " , (2-3);
print "\n2 x 3 = " , (2*3);
print "\n2 / 3 = " , (2/3);
print "\n18 mod 5 = " , (18%5); # reste de la divison de 18 par 5

# puissances : **
print "\n2^3 = " , (2**3); # 2 puissance 3, affiche 8

# x pour répéter
print "\n" , 5 x 4; # x est opérateur de répétition, affiche 5555

print "\n" , "toto" x 4; # affiche totototototototo

